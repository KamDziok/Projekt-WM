<?php
include_once 'model/Aktualnosci.php';

use PHPUnit\Framework\TestCase;
class AktualnosciTest extends TestCase {
    public function testAktualnosci_aktualnosc_liczba(){
        try{
        $aktualnosci = new Aktualnosci(54245);
        }catch(Exception $e){
        $this->assertEquals('argument nie jest typu String', $e->getMessage());
        }
    }
    public function testAktualnosci_aktualnosc_null(){
        try{
        $aktualnosci = new Aktualnosci(null);
        }catch(Exception $e){
        $this->assertEquals('argument nie został podany', $e->getMessage());
        }
    }
    public function testAktualnosci(){
        $wiadomosci="Wiadomości ze świata filmów:/nTest ";
        $aktualnosci = new Aktualnosci($wiadomosci);
        $this->assertIsString($wiadomosci);
    }
}